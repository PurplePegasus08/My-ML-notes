import random
jackpot = random.randint(1,50)

user = int(input("enter your number"))
counter = 1

while jackpot != user:
    if user > jackpot:
        print('guess lower')
        

    else:
        print(' guess higher ')
    user = int(input("enter your number"))
    counter +=1

print('correct number')
print('you took',counter,'steps')

